package java_wk2;

public class Break_switch {

	public static void main(String[] args) {

		//(tried)String name = "Matt";
		int age = 4;
		
		switch (age) {
		
		case 1:
		System.out.println("you can crawl");
		break;
		
		case 2:
		System.out.println("you can talk");
		break;
		
		case 3:
		System.out.println("you can walk");
		break;
		
		case 4:
		System.out.println("you can run");
		break;
		
		case 5:
		System.out.println("you can go to school");
		break;
		
		case 6:
		System.out.println("you should do your homework");
		break;
		
		default:
		System.out.println("I dont know your age");
		break;
		}

	}

}
